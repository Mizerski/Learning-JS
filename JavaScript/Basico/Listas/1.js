/*
    Listas no javascript são chamadas de arrays, e são usadas para armazenar varios valores em uma unica variavel.

    Arrays são usados para armazenar varios valores em uma unica variavel.

    Elas sao particulamente uteis quando voce quer armazenar varios valores em uma unica variavel.

    Estrutura de um array:

    let nomeDoArray = [valor1, valor2, valor3, ...]

*/

// Exemplo de um array basico

let frutas = ['Maçã', 'Banana', 'Laranja', 'Uva']

console.log(frutas) // [ 'Maçã', 'Banana', 'Laranja', 'Uva' ]

// Exemplo de um array com numeros

let numeros = [1, 2, 3, 4, 5]

console.log(numeros) // [ 1, 2, 3, 4, 5 ]

// Exemplo de um array com objetos

let pessoas = [
    { nome: 'João', idade: 30 },
    { nome: 'Maria', idade: 25 },
    { nome: 'José', idade: 40 }
]


/*
    Matrizes no javascript sao arrays de arrays, e sao usadas para armazenar varios valores em uma unica variavel.

    Matrizes sao usadas para armazenar varios valores em uma unica variavel.

    Elas sao particulamente uteis quando voce quer armazenar varios valores em uma unica variavel.

*/

// Exemplo de uma matriz basica

let matriz = [
    [1, 2, 3],
    [4, 5, 6],
    [7, 8, 9]
]

console.log(matriz) // [ [ 1, 2, 3 ], [ 4, 5, 6 ], [ 7, 8, 9 ] ]



