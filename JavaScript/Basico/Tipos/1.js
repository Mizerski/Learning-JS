/*
    Tipos de dados em JavaScript

    Em JavaScript, existem vários tipos de dados, mas os principais são:

    - String -> usado para armazenar texto
    - Number -> usado para armazenar números
    - Boolean -> usado para armazenar verdadeiro ou falso
    - Object -> usado para armazenar objetos
    - Array -> usado para armazenar listas
    - Function -> usado para armazenar funções
    - Undefined -> usado para armazenar valores indefinidos
    - Null -> usado para armazenar valores nulos
*/

/*

    Em JavaScript, existem dois tipos de dados: primitivos e não primitivos.

    Os tipos primitivos são: string, number, boolean, undefined, null, symbol e bigint.

    Os tipos não primitivos são: object e function.

    Os tipos primitivos são imutáveis, ou seja, não podem ser alterados.

    Os tipos não primitivos são mutáveis, ou seja, podem ser alterados.

    Exemplos de tipos primitivos:

    let nome = 'João'

    let idade = 30

    let sexo = true

    let cidade = undefined

    let estado = null

    let simbolo = Symbol('simbolo')

    let numeroGrande = 9007199254740991n

    Exemplos de tipos não primitivos:

    let pessoa = { nome: 'João', idade: 30 }

    function somar(a, b) {
        return a + b
    }

    No exemplo acima, a variável pessoa é um objeto, e a variável somar é uma função.

*/