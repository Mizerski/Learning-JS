// Declarando duas variáveis: 'name' para armazenar o nome "John" e 'age' para armazenar a idade 25.
let name = "John";
let age = 25;

// Exibindo o valor da variável 'name' no console.
console.log(name);
// Exibindo o valor da variável 'age' no console.
console.log(age);
// Exibindo uma mensagem composta no console, que inclui o valor da variável 'name' e 'age'.
console.log("My name is", name, "and I'm", age, "years old.");

//===================================================================================================

// Declarando uma variável 'lastName' sem atribuir um valor inicial a ela.
let lastName;
// Atribuindo o valor "Doe" à variável 'lastName'.
lastName = "Doe";
// Exibindo o valor da variável 'lastName' no console.
console.log("And my last name is", lastName);
